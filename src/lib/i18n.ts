export function isLocale(value: string | undefined | null): value is Locale {
  return Boolean(value && (locales as readonly string[]).includes(value));
}

export function normalizeLocale(value: string | undefined | null): Locale {
  return isLocale(value) ? value : "ru";
}

export function t(locale: Locale, key: string, values?: Record<string, string | number>) {
  const template = dictionaries[locale][key] ?? dictionaries.ru[key] ?? key;

  if (!values) {
    return template;
  }

  return Object.entries(values).reduce((text, [name, value]) => text.replaceAll(`{${name}}`, String(value)), template);
}

export function translateCategory(locale: Locale, category: string) {
  const key = categoryKeys[category];
  return key ? t(locale, key) : category;
}

function translateFields(locale: Locale, module: RazbyModule) {
  const text = locale === "ru" ? undefined : moduleTexts[locale][module.slug];

  return module.fields.map((field) => ({
    ...field,
    ...(locale === "ru" ? {} : { label: fieldTexts[locale][field.name] ?? field.label }),
    ...(text?.fields?.[field.name] ?? {}),
  }));
}

export function getLocalizedModules(locale: Locale) {
  return modules.map((module) => {
    const text = locale === "ru" ? undefined : moduleTexts[locale][module.slug];

    return {
      ...module,
      title: text?.title ?? module.title,
      shortTitle: text?.shortTitle ?? module.shortTitle,
      categoryLabel: translateCategory(locale, module.category),
      description: text?.description ?? module.description,
      outcome: text?.outcome ?? module.outcome,
      fields: translateFields(locale, module),
    };
  });
}

export function getLocalizedModule(slug: string, locale: Locale) {
  return getLocalizedModules(locale).find((module) => module.slug === slug);
}

export function getLocalizedNavigationGroups(locale: Locale) {
  return navigationGroups.map((group) => ({
    ...group,
    title: t(locale, groupTitleKeys[group.title] ?? group.title),
  }));
}

